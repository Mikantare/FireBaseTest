public class User {

    private String name;
    private String lastName;
    private String age;
    private String man;

    public User() {
    }

    public User(String name, String lastName, String age, String man) {
        this.name = name;
        this.lastName = lastName;
        this.age = age;
        this.man = man;
    }
}
